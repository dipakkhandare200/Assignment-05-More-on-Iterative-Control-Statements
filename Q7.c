#include<stdio.h>
int main()
{
    int i;
    int k;
    printf(" how many even number you want in reverse order =");
    scanf(" %d",&k);
     for(i=k;i>=1;i--)
    {

        printf("\n %d",2*i);



    }

    return 0;
}








